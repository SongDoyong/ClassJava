
/*
	 << , >>

		10  <<  2
		----
		1010 --> 1010(_)(_) --> 101000 --> 40
		
		7 << 3
		111 --> 111000 --> 56 (32 + 16 + 8)
		
		8 << 2
		1000 -> 100000 --> 32 
		
		50 << 3
		110010 --> 11001000 --> 128 + 64 + 8 = 200
		
		x<<y ==> x*2^y
		9<<3 --> 9 x 2^3 --> 9*8 --> 72
		
		======================================
		10    >>    2 
		----
		1010
		  --제거 --> 10 --> 2 (뒤에서부터 제거)
		  
		15 << 3
		1111 --> 1

		27>>3
		11011 --> 11
		
		x >> y 
		x / 2^y 
		
		100>>3
		100 / 8 --> 12 (정수값만)

*/
public class _03_쉬프트연산자 {
	public static void main(String[] args) {
		System.out.println(10<<2); //40
		System.out.println(7<<3); //56
		System.out.println(8<<2); // 32
		System.out.println(15>>3); // 1
		System.out.println(10>>2); // 2
	}
	
}
